<?php

declare(strict_types=1);

namespace App\Support;

/**
 * Единая проверка «похоже ли это на имя клиента». Используется И детерминированным
 * разбором (`ContactGate`), И LLM-путём (`ContactCapture`/`NameDetector`), чтобы
 * частые ответы-не-имена не сохранялись как имя (прод-баг «а меня нет в базе?» →
 * имя «Нет»; «Любой мастер» → имя «Любой Мастер»).
 */
final class NameValidator
{
    /** Слова, которые именем НЕ являются (ответы/вопросы/услуги/команды). */
    private const array STOPWORDS = [
        'нет', 'да', 'не', 'ок', 'окей', 'угу', 'ага', 'неа', 'хз', 'нету',
        'привет', 'здравствуйте', 'здравствуй', 'добрый', 'спасибо', 'пожалуйста',
        'как', 'что', 'кто', 'где', 'когда', 'почему', 'зачем', 'сколько', 'какой', 'какая',
        'можно', 'хочу', 'надо', 'нужно', 'записаться', 'запишите', 'запиши', 'запись', 'записи',
        'отмени', 'отмена', 'перенеси', 'перенос', 'любой', 'любая', 'свободный', 'мастер',
        'услуга', 'услугу', 'стрижка', 'маникюр', 'педикюр', 'борода',
        'база', 'базе', 'базу', 'есть', 'тут', 'здесь', 'уже', 'вас', 'тебя', 'меня', 'вообще',
    ];

    /**
     * Принимать ли $name как имя клиента. $sourceText — исходное сообщение
     * (если это вопрос — имя не берём).
     */
    public static function isPlausible(?string $name, string $sourceText): bool
    {
        if ($name === null || trim($name) === '') {
            return false;
        }

        // Вопрос — это не представление.
        if (str_contains($sourceText, '?')) {
            return false;
        }

        $words = preg_split('/\s+/u', trim(mb_strtolower($name))) ?: [];

        // Имя — это 1–3 слова (имя/фамилия/отчество), не предложение.
        if ($words === [] || count($words) > 3) {
            return false;
        }

        foreach ($words as $word) {
            // Любое слово-стоп-слово или слишком короткое (1 буква) → не имя.
            if (in_array($word, self::STOPWORDS, true) || mb_strlen($word) < 2) {
                return false;
            }
        }

        return true;
    }
}
