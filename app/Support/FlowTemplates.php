    /**
     * @param  list<string>  $triggers
     * @param  array<string, mixed>  $nodes
     * @return array{key: string, name: string, description: string, businessType: string|null, triggers: list<string>, definition: array<string, mixed>}
     */
    private static function tpl(string $key, string $name, string $description, ?string $businessType, array $triggers, string $start, array $nodes): array
    {
        return [
            'key' => $key,
            'name' => $name,
            'description' => $description,
            'businessType' => $businessType,
            'triggers' => $triggers,
            'definition' => ['start' => $start, 'nodes' => $nodes],
        ];
    }

    /**
     * @param  list<array{label: string, next: string}>  $options
     * @return array<string, mixed>
     */
    private static function msg(string $text, string $action, array $options, int $x, int $y): array
    {
        return ['type' => 'message', 'action' => $action, 'text' => $text, 'options' => $options, 'position' => ['x' => $x, 'y' => $y]];
    }

    /**
     * @return array<string, mixed>
     */
    private static function input(string $text, string $variable, string $next, int $x, int $y): array
    {
        return ['type' => 'input', 'text' => $text, 'variable' => $variable, 'next' => $next, 'position' => ['x' => $x, 'y' => $y]];
    }

    /**
     * @return array{label: string, next: string}
     */
    private static function opt(string $label, string $next): array
    {
        return ['label' => $label, 'next' => $next];
    }
