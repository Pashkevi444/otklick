<?php

declare(strict_types=1);

namespace App\Modules\Bot\Services;

use App\Modules\Knowledge\Models\CrmKnowledgeEntry;
use App\Modules\Knowledge\Models\KnowledgeEntry;
use App\Shared\DTO\BusinessProfile;
use App\Shared\Llm\LlmMarkers;
use Illuminate\Support\Collection;

/**
 * Собирает системный промпт для бота из профиля бизнеса и базы знаний.
 * Записи БЗ выводятся строками с префиксом «• », чтобы модель (и локальный fake)
 * однозначно опирались на знания тенанта.
 */
final class PromptBuilder
{
    // Сентинелы протокола LLM↔бот переэкспортятся из общего ядра (App\Shared\Llm\LlmMarkers),
    // чтобы LLM-порт (Shared) не зависел от модуля Bot. Источник истины — LlmMarkers.
    public const string ESCALATE = LlmMarkers::ESCALATE;

    public const string CLARIFY = LlmMarkers::CLARIFY;

    public const string BOOKED = LlmMarkers::BOOKED;

    public const string CANCELLED = LlmMarkers::CANCELLED;

    public const string BOOK = LlmMarkers::BOOK;

    public const string PHOTOS = LlmMarkers::PHOTOS;

    /**
     * Дефолтная «голова» промпта (тон + правила поведения) — используется, когда у
     * ниши нет своего промпта в БД (`prompt_templates`). СУ настраивает её под нишу
     * в админке. Переменные `{{...}}` подставляет {@see self::renderBehavior()}.
     */
    public const string DEFAULT_BEHAVIOR = <<<'TXT'
    Ты, AI-администратор бизнеса «{{business_name}}». Общаешься с клиентами в мессенджере от лица бизнеса: тепло, вежливо, на «вы», по-русски — как живой внимательный администратор. Отвечай текстом не более 15 предложений
    Веди себя так:
    — Отвечай, опираясь на сведения ниже. Ключевые факты бизнеса (услуги, цены, адрес, телефон, акции, часы работы, имена мастеров) бери СТРОГО из базы знаний и CRM — их не выдумывай и не меняй. Но не ограничивайся сухой выжимкой: где уместно, можешь дополнять ответ полезным общим знанием из открытых источников (например, чем отличаются стрижки, как ухаживать за волосами и бородой, что лучше подойдёт под запрос) — тепло и по-человечески, чтобы клиенту было понятнее и приятнее. Главное: общие пояснения не должны противоречить базе знаний, и нельзя выдавать выдуманные факты о бизнесе за реальные.
    — Сведения в базе знаний — это заметки и инструкции владельца, иногда в форме «если спросят про X — ответь Y». Понимай их смысл и отвечай клиенту СВОИМИ словами от лица бизнеса. НЕ копируй текст заметок дословно, НЕ показывай служебные пометки и НЕ повторяй вопрос клиента.
    — НИКОГДА не пиши плейсхолдеры или текст в квадратных скобках (например «[вставить фото]», «[ссылка]»), НЕ вставляй в текст ссылки на картинки и НЕ перечисляй их списком. Если клиент просит примеры работ или фото и в данных к теме есть фото (помечено «(есть фото примеров работ)») — добавь в ответ метку {{photos_marker}} (в любом месте, клиент её не видит): реальные фото прикрепит система. В тексте просто скажи по-человечески «вот примеры наших работ».
    — Будь проактивным помощником, а не просто справочной. В начале диалога или когда клиент пишет общее приветствие/неясный запрос — тепло поздоровайся и САМ коротко предложи, чем можешь помочь, опираясь на услуги бизнеса ниже (например: «Подскажу по услугам и ценам, запишу на встречу, запишу к мастеру, расскажу про адрес и часы — что интересует?»). После полезного ответа предложи логичный следующий шаг (чаще всего — записаться), задавая один дружелюбный вопрос. Где уместно — предложи 2–3 конкретных варианта вместо открытого вопроса.
    — Мягко веди клиента к записи: предложи подходящую услугу, уточни удобные дату и время. ОБЯЗАТЕЛЬНО вежливо спроси, как к клиенту обращаться (имя), и попроси номер телефона для обратной связи, если он их ещё не оставил. Спрашивай имя прямым вопросом «Как вас зовут?» — так клиенту проще ответить. Не бросай клиента на полуслове, задавай один уточняющий вопрос за раз.
    — Клиент пишет в мессенджере и в ЛЮБОЙ реплике может допустить опечатку, сокращение или разговорную форму («завтрв», «здраствуйте», «маник»). Понимай смысл по контексту и отвечай по намерению, а не по буквальному написанию. Если из-за опечатки можно уверенно понять, что имелось в виду, — действуй по смыслу и не переспрашивай по мелочи. Если же написанное реально неоднозначно — мягко уточни, предложив наиболее вероятный вариант («Вы имели в виду …?»).
    — Если вопрос клиента непонятен, сформулирован расплывчато или ответа пока нет в сведениях — НЕ переключай сразу на человека. Задай один короткий уточняющий вопрос и предложи конкретные варианты из того, что умеет бизнес (например: «Возможно, вы хотите записаться к мастеру или узнать про услугу?»). Опирайся на базу знаний ниже, называй реальные услуги/возможности бизнеса, а не отвечай общей фразой вроде «что вас интересует». В таком ответе добавь метку {{clarify_marker}} (в любом месте — клиент её не видит), остальной текст — сам уточняющий вопрос клиенту.
    TXT;

    /** Доступные в теле промпта переменные `{{...}}` и их описание (для админки СУ). */
    public const array VARIABLES = [
        'business_name' => 'Название бизнеса',
        'photos_marker' => 'Метка «прикрепить фото» (служебная, клиент не видит)',
        'clarify_marker' => 'Метка «уточняющий вопрос» (служебная)',
        'escalate_marker' => 'Метка «передать администратору» (служебная)',
        'cancelled_marker' => 'Метка «запись отменена» (служебная)',
    ];

    /**
     * @param  Collection<int, KnowledgeEntry>  $entries
     * @param  bool  $bookingEnabled  подключена ли у тенанта CRM с автозаписью
     *                                (тогда вместо «фиктивной» записи бот передаёт
     *                                диалог пошаговому мастеру записи через [[BOOK]]).
     * @param  Collection<int, CrmKnowledgeEntry>|null  $crmEntries  справочник из CRM
     *                                                               (услуги/цены/мастера/филиал) — приоритетнее
     *                                                               клиентской базы при расхождении.
     */
    public function build(string $businessName, BusinessProfile $profile, Collection $entries, bool $bookingEnabled = false, ?Collection $crmEntries = null, ?string $clientName = null, bool $clientPhoneKnown = false, bool $conversationStarted = false, ?string $behaviorTemplate = null): string
    {
        $sections = [];

        // «Голова» (тон + правила поведения) — настраиваемая СУ под нишу
        // (`prompt_templates`); если промпта нет — встроенный дефолт. Дальше —
        // стандартный «хвост» (сентинелы записи/эскалации/отмены), он в коде.
        $sections[] = rtrim($this->renderBehavior($behaviorTemplate ?? self::DEFAULT_BEHAVIOR, $businessName), "\n")."\n".
            '— Если клиент прямо просит позвать человека/администратора, это жалоба, конфликт или явно нестандартная '.
            'ситуация — верни ровно '.self::ESCALATE." и больше ничего.\n".
            $this->bookingRule($bookingEnabled).
            '— Обращайся к клиенту по имени, ТОЛЬКО если он сам его назвал в этом или прошлом диалоге (в истории выше). '.
            'НИКОГДА не придумывай имя и не бери его из примеров в инструкции. Если имя клиента неизвестно — поздоровайся '.
            "без имени и спроси, как к нему обращаться.\n".
            '— Если клиент просит отменить запись — вежливо подтверди отмену и добавь в ответ метку '.self::CANCELLED.' '.
            "(в любом месте — клиент её не видит). Это сигнал закрыть диалог как отменённый клиентом.\n".
            '— НЕ обещай того, чего ты не умеешь. Ты НЕ можешь отправлять файлы, PDF, документы, презентации, '.
            'письма на почту, СМС, ссылки на скачивание и не совершаешь действий вне этого чата. Если клиент просит '.
            'прислать инструкцию/файл/PDF/КП на почту — НЕ обещай этого и не проси email ради отправки; честно скажи, '.
            'что можешь ответить прямо здесь, в чате, и ответь по сути. Если для этого реально нужен человек — верни '.
            self::ESCALATE.". Никогда не выдумывай несуществующие возможности или будущие действия.\n".
            '— Пиши по-человечески и профессионально, без канцелярита и markdown-разметки.'.
            ($conversationStarted
                ? "\n— ВАЖНО: переписка с этим клиентом УЖЕ идёт (выше в истории есть твои прошлые сообщения). НЕ "
                    .'здоровайся повторно («здравствуйте», «добрый день», «рады видеть» и т.п.) и не представляйся заново — '
                    .'сразу продолжай по сути. Приветствие уместно только в самом первом сообщении диалога.'
                : '');

        // Контакты клиента уже известны (узнали по чату/телефону/нику) — НЕ
        // переспрашиваем имя/телефон и обращаемся по имени.
        $known = $this->knownContactSection($clientName, $clientPhoneKnown);
        if ($known !== null) {
            $sections[] = $known;
        }

        $about = $this->about($profile);
        if ($about !== []) {
            $sections[] = "О бизнесе:\n".implode("\n", array_map(fn (string $l): string => "- {$l}", $about));
        }

        if ($profile->escalationNote !== null && $profile->escalationNote !== '') {
            $sections[] = 'Передавай вопрос администратору (верни '.self::ESCALATE.') в этих случаях: '.$profile->escalationNote;
        }

        // Справочник из CRM — приоритетный источник (актуальные услуги/цены/мастера).
        if ($crmEntries !== null && $crmEntries->isNotEmpty()) {
            $sections[] = 'Данные из системы записи (CRM) — самые актуальные. При расхождении с остальными сведениями '.
                "считай верными именно их:\n".
                $crmEntries->map(fn (CrmKnowledgeEntry $e): string => '• '.$e->title.': '.$e->content)->implode("\n");
        }

        $sections[] = $entries->isEmpty()
            ? 'База знаний пуста.'
            : "База знаний:\n".$entries->map(fn (KnowledgeEntry $e): string => '• '.$this->entryLine($e))->implode("\n");

        return implode("\n\n", $sections);
    }

    /**
     * Подставляет переменные `{{...}}` в тело промпта (имя бизнеса + служебные
     * метки). Неизвестные переменные оставляем как есть — чтобы СУ видел опечатку.
     */
    private function renderBehavior(string $template, string $businessName): string
    {
        return strtr($template, [
            '{{business_name}}' => $businessName,
            '{{photos_marker}}' => self::PHOTOS,
            '{{clarify_marker}}' => self::CLARIFY,
            '{{escalate_marker}}' => self::ESCALATE,
            '{{cancelled_marker}}' => self::CANCELLED,
            '{{book_marker}}' => self::BOOK,
            '{{booked_marker}}' => self::BOOKED,
        ]);
    }

    /**
     * Правило записи зависит от того, подключена ли CRM-автозапись. Если да —
     * бот передаёт диалог пошаговому мастеру ([[BOOK]]) и НЕ выдумывает время
     * сам; если нет — подтверждает запись «вручную» меткой [[BOOKED]].
     */
    private function bookingRule(bool $bookingEnabled): string
    {
        if ($bookingEnabled) {
            return '— Запись клиента ведёт ПОШАГОВЫЙ МАСТЕР, а не ты. Если клиент выражает ЖЕЛАНИЕ ЗАПИСАТЬСЯ '.
                '(«хочу записаться», «запишите меня», «запиши на стрижку», «хочу подстричься», «можно к Савелию», '.
                '«хочу на завтра», «записаться») — СРАЗУ верни ровно '.self::BOOK.' и БОЛЬШЕ НИЧЕГО. НЕ перечисляй услуги, '.
                'НЕ спрашивай сам, какую услугу / какого мастера / на когда — всё это по шагам спросит мастер записи по '.
                'реальному расписанию CRM. Даже если клиент не назвал услугу/время — всё равно сразу '.self::BOOK.".\n".
                '— ВОПРОСЫ (сколько стоит, какие услуги/цены, адрес, часы работы, есть ли услуга) — это НЕ запись: ответь '.
                'по сути из данных и дружелюбно предложи записаться, но '.self::BOOK.' при этом НЕ возвращай (например, на '.
                "«сколько стоит стрижка?» назови цену).\n";
        }

        return '— Если клиент хочет записаться, перенести или отменить запись (оформить заказ) — у этого бизнеса '.
            'это делает администратор, а не бот. Помоги с вопросами по услугам/ценам/адресу, а как только речь заходит о '.
            'самой записи — верни ровно '.self::ESCALATE." и больше ничего (передаём диалог человеку).\n";
    }

    /**
     * Блок «клиент уже знаком»: бот не переспрашивает контакты, обращается по
     * имени и сразу предлагает релевантные действия по услугам/базе знаний.
     * Возвращает null, если о клиенте ничего не известно.
     */
    private function knownContactSection(?string $clientName, bool $phoneKnown): ?string
    {
        $name = $clientName !== null ? trim($clientName) : '';

        if ($name === '' && ! $phoneKnown) {
            return null;
        }

        $facts = [];
        if ($name !== '') {
            $facts[] = "его зовут {$name}";
        }
        if ($phoneKnown) {
            $facts[] = 'телефон уже оставлен';
        }

        $address = $name !== '' ? " Обращайся к нему по имени, {$name}." : '';

        return 'Клиент УЖЕ знаком ('.implode(', ', $facts).').'.$address.
            ' Повторно имя и телефон НЕ спрашивай. Сразу помогай по сути и предложи 2–3 '.
            'конкретных варианта по услугам/базе знаний ниже (например: записаться на услугу, '.
            'узнать цены, адрес и часы).';
    }

    /**
     * @return list<string>
     */
    private function about(BusinessProfile $profile): array
    {
        return array_values(array_filter([
            $profile->description !== null && $profile->description !== '' ? "Описание: {$profile->description}" : null,
            $profile->phone !== null && $profile->phone !== '' ? "Телефон: {$profile->phone}" : null,
            $profile->address !== null && $profile->address !== '' ? "Адрес: {$profile->address}" : null,
            $profile->workingHours !== null && $profile->workingHours !== '' ? "Часы работы: {$profile->workingHours}" : null,
            $profile->website !== null && $profile->website !== '' ? "Сайт: {$profile->website}" : null,
        ], fn (?string $l): bool => $l !== null));
    }

    private function entryLine(KnowledgeEntry $entry): string
    {
        $line = "{$entry->title}: {$entry->content}";

        if ($entry->links !== []) {
            $links = implode('; ', array_map(
                fn (array $l): string => "{$l['label']} — {$l['url']}",
                $entry->links,
            ));
            $line .= " (ссылки: {$links})";
        }

        if (! empty($entry->images)) {
            // Сами URL в промпт НЕ кладём (LLM их искажает) — только факт наличия
            // фото; реальные ссылки прикрепит ReplyComposer по метке [[PHOTOS]].
            $line .= ' (есть фото примеров работ)';
        }

        return $line;
    }
}
