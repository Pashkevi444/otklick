<?php

declare(strict_types=1);

use App\Models\User;
use Illuminate\Support\Facades\Broadcast;

/*
 * Приватные каналы реалтайма (Reverb).
 *
 * ИЗОЛЯЦИЯ ТЕНАНТОВ — критично (NFR «изоляция 100%»): пользователь подписывается
 * ТОЛЬКО на канал СВОЕГО тенанта. По каналу гоняем не данные под право, а лёгкий
 * сигнал «обновись» — клиент по нему перезапрашивает свою персональную выдачу
 * уведомлений/списков через обычный авторизованный HTTP-эндпоинт, который уже
 * фильтрует по `MemberPermission`. Так за WS не утекают данные мимо прав, а
 * изоляция тенантов держится на авторизации канала.
 *
 * (Супер-админ при импорсонации действует как владелец — у него выставлен
 * tenant_id, поэтому отдельного байпаса не нужно.)
 */
Broadcast::channel('tenant.{tenantId}', function (User $user, string $tenantId): bool {
    return $user->tenant_id !== null && (string) $user->tenant_id === (string) $tenantId;
});
