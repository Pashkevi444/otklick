def business_plan(num, title, recommended, data):
    title_text = f'{num}. Бизнес-план: {title}'
    h1(title_text)
    if recommended:
        callout('★ РЕКОМЕНДОВАННЫЙ ВАРИАНТ под твои вводные', fill='E6F4EA', color=GREEN)

    h2('Суть')
    para(data['essence'])

    h2('Целевой клиент')
    para(data['client'])

    h2('Проблема и ценность (почему платят)')
    for b in data['value']:
        bullet(b)

    h2('Продукт: функционал MVP')
    for b in data['mvp']:
        bullet(b)

    h2('Технологический стек')
    para(data['stack'])

    h2('Модель монетизации')
    for b in data['money']:
        bullet(b)

    h2('Юнит-экономика и путь к 1 млн ₽/мес')
    para(data['econ'])
    if data.get('econ_table'):
        make_table(data['econ_table'][0], data['econ_table'][1], data['econ_table'][2])

    h2('Go-to-market: как добыть первых 10 клиентов без бюджета')
    for i, b in enumerate(data['gtm']):
        numbered(b)

    h2('Стартовые затраты')
    para(data['costs'])

    h2('Таймлайн до значимого дохода')
    para(data['timeline'])

    h2('Риски и митигация')
    for risk, mit in data['risks']:
        bullet(mit, bold_prefix=risk + ' → ')

    h2('Оценка под твои вводные')
    callout(data['verdict'], fill='FFF4E5' if not recommended else 'E6F4EA',
            color=ACCENT2 if not recommended else GREEN)
    doc.add_page_break()

